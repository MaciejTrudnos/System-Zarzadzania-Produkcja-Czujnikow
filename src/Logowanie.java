    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   if(jTextField1.getText().length()==0) 
      JOptionPane.showMessageDialog(null, "Pole użytkownik jest puste. Uzupełnij");
   else if(jPasswordField1.getPassword().length==0)  
      JOptionPane.showMessageDialog(null, "Pole hasło jest puste. Uzupełnij");
   else{
       String user = jTextField1.getText();   
       char[] pass = jPasswordField1.getPassword(); 
       String pwd = String.copyValueOf(pass);  

     if(validate_login_pracownik(user,pwd) && !jCheckBox1.isSelected()){                                         
  
        panelPracownika pracownik = new panelPracownika();
        pracownik.setVisible(true);
        dispose();
    }
     
           else if(validate_login(user,pwd) && jCheckBox1.isSelected()){                                         
  
        panelAdministratora s = new panelAdministratora();
        dispose();
        
         s.setVisible(true);
    
    }
       
       else
          JOptionPane.showMessageDialog(null, "Nie ma takiego użytkownika");
   }  
    }//GEN-LAST:event_jButton1ActionPerformed

