    private boolean validate_login(String username,String password) {
        try{           
       Class.forName("com.mysql.jdbc.Driver");  
            Connection con = DriverManager.getConnection( "jdbc:mysql://localhost:3306/administrator?zeroDateTimeBehavior=convertToNull", "root", "" );  
            java.sql.PreparedStatement pst = con.prepareStatement("Select * from logowanie where Uzytkownik=? and Haslo=?");
       pst.setString(1, username); 
       pst.setString(2, password);
       ResultSet rs = pst.executeQuery();                        
       if(rs.next())            
           return true;    
       else
           return false;            
   }
   catch(Exception e){
       e.printStackTrace();
       return false;
   }       
    }
    
